package org.binary;

public class BSTNode {

    BSTNode left, right;

    int data;
    /* Constructor */

    public BSTNode()
    {
        left = null;
        right = null;
        data = 0;
    }
    /* Constructor */
    public BSTNode(int n)
    {
        left = null;
        right = null;
        data = n;
    }         
}
